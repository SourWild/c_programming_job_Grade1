   /***************************************************************************
	 函数名称：
	 功    能：返回n阶legendre多项式的值
	 输入参数：
	 返 回 值：
	 说    明：函数名、形参、返回类型均不准动
   ***************************************************************************/
double legendre(double x, int n)
{
	if (n == 0)
		return 1;
	if (n == 1)
		return x;
	else {
		return ((2 * (double)n - 1) * x * legendre(x, n - 1) - ((double)n - 1) * legendre(x, n - 2)) / n;
	}
}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：完成输入、调用递归函数、输出
***************************************************************************/
int main()
{
	int n;
	double x, zhi;
	cout << "计算legendre，请输入x和n的值\n";
	cin >> x >> n;
	if (n >= 0) {
		zhi = legendre(x, n);
		cout << "legendre[" << n << "](" << x << ")=" << zhi << endl;
	}
	cout << endl;
	return 0;
}
